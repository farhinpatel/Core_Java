package Operators;

public class RelationalOperators {

	public static void main(String[] args) {
		// <,>,<=,>=,!=,==
		
		System.out.println(6<9);
		System.out.println(3>5);
		
		int a=45,b=80;
		
		System.out.println(a<b);
		
		System.out.println(a!=b);
		
		System.out.println(a==b);

	}

}
